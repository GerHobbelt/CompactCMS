<h1>The features that make CompactCMS</h1>
<h2>Philosophy</h2>
<p>Most Content Management Systems in my opinion try to hard to cover as many features and possibilities at once. Causing the actual end-user to have a hard time actually <em>managing content</em>. The CompactCMS project focuses on the ease of content management for the end-user, while providing a flexible framework for the designer or developer. See the <a href="http://demo.compactcms.nl/admin/" rel="external" target="_blank">demonstration</a> to experience CompactCMS first hand.</p>
<div class="span-14">
<div class="span-7">
